# Printing the sum, product of 1 to 10 counting....
i = 1
sum = 0
product = 1
while i <= 10:
    print(i)
    sum = sum + i
    product = product * i
    i += 1
print("-------------------------")
print("Sum of 1 to 10     : ",sum)
print("-----------------------------")
print("Product of 1 to 10 : ",product)