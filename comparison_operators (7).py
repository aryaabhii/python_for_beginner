# Comparison operators in python

# we use comparsion operator for compasriosn
# 1.) < less than
# 2.) <= less than or equal
# 3.) > greater than
# 4.) >= greater than or equal
# 5.) == equal 
# 6.) != not equal


#examples:
var = 3 > 2 # here 3 is greater than 2 so we got result boolean true
print(var) 

var = 3 >= 2 # here 3 is greater than 2 so we got result boolean true
print(var) 

var = 3 < 2 # here 2 is less than 3 so we got result boolean false
print(var) 

