# Logical operators in python

# we use this operators to built complex role and conditions.

# 1.) and (both)
# 2.) or (at least one)
# 3.) not (inverses that we have any value which we given)

price = 33
print (price > 10 and price < 33) # result false
print (price > 10 or price < 33)  # result true
print (price > 10) # result true
print (not price > 10) # result false 

